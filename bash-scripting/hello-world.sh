#!/bin/bash
# The above is the shebang, and it informs the OS of the path of the interpreter.
echo "Hello, world! - 1"
# This prints Hello, world! - 1 to the terminal.
echo "Hello..."; echo "world! - 2"
# As you can see, we can chain same-line commands with a semicolon.
name="earth"
echo "Hello, $name!"
# Look, this time we assigned a value to a variable and used that variable in echo's argument.
